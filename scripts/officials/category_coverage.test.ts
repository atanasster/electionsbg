describe("category coverage", () => {
  it.each(REGISTER_CATEGORIES)("maps %s", (name, expected) => {
    expect(categoriseRaw(name)).toBe(expected);
  });

  // The map keys on substrings of names that share long legal preambles. A
  // category matching two buckets resolves by array order, not by meaning —
  // which is a latent reclassification waiting for someone to reorder the map.
  it("assigns each register category to at most one bucket", () => {
    const ambiguous: string[] = [];
    for (const [name] of REGISTER_CATEGORIES) {
      const hits = CATEGORY_MAP.filter((b) =>
        b.substrings.some((s) => name.includes(s)),
      ).map((b) => b.kind);
      if (new Set(hits).size > 1) {
        ambiguous.push(
          `${name.slice(0, 60)}… → ${[...new Set(hits)].join(", ")}`,
        );
      }
    }
    expect(ambiguous).toEqual([]);
  });

  // Bucket-level reachability misses a subtler failure: an individual substring
  // that matches nothing. It looks like coverage in review and contributes none,
  // which is how "изпълнителни агенции" sat in the map as decoration.
  it("has no substring that matches no register category", () => {
    const dead = CATEGORY_MAP.flatMap((b) =>
      b.substrings.filter(
        (sub) => !REGISTER_CATEGORIES.some(([name]) => name.includes(sub)),
      ),
    );
    expect(dead).toEqual([]);
  });

  // Every bucket in the union should be reachable; an unreachable one means a
  // substring was mistyped and its declarants are silently unfetched.
  it("leaves no bucket unreachable", () => {
    const reached = new Set(
      REGISTER_CATEGORIES.map(([n]) => categoriseRaw(n)).filter(Boolean),
    );
    const declared = new Set(CATEGORY_MAP.map((b) => b.kind));
    // deputy_minister is reached via the position title, not a category.
    const unreachable = [...declared].filter((k) => !reached.has(k));
    expect(unreachable).toEqual([]);
  });
});
